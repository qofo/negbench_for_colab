"""
reprocess_llava_predictions.py

Post-processes a LLaVA MCQ predictions CSV produced by eval_negation_llava.py.
Fixes three issues found in the raw output:

  1. PARSING BUG: The default parser uses a plain substring search, which always
     finds the letter 'A' in almost any English sentence (e.g. "Answer", "A cat",
     etc.). This makes the model appear to always predict option A regardless of
     what it actually generated.  This script replaces that logic with robust
     pattern-based parsing that handles outputs like "(B)", "The answer is C",
     "Option D", etc.

  2. SHUFFLED ORDER: The predictions CSV stores captions in the shuffled order
     that was presented to the model, not in the original dataset order.
     This script restores the original order (caption_0 = ground-truth answer,
     caption_1 = hybrid, caption_2 = positive, caption_3 = negative) and adjusts
     correct_answer / predicted_answer indices accordingly.

  3. LOGITS (optional): When --llava-model-path is supplied the script re-runs
     the model with output_scores=True and extracts the probability of each
     option letter (A/B/C/D) from the first generated token.  The probabilities
     are stored as logit_0 .. logit_{n-1} in the original caption order.

Usage
    Fast mode (no model, fixes 1 & 2 only):
        python -m src.evaluation.reprocess_llava_predictions \\
            --input-csv  logs/evaluation/mcq_llava/predictions/coco-mcq_predictions.csv \\
            --dataset-csv data/images/COCO_val_mcq_llama3.1_rephrased.csv \\
            --output-csv  logs/evaluation/mcq_llava/predictions/coco-mcq_reprocessed.csv

    Full mode (fixes 1, 2 & 3):
        python -m src.evaluation.reprocess_llava_predictions \\
            --input-csv  logs/evaluation/mcq_llava/predictions/coco-mcq_predictions.csv \\
            --dataset-csv data/images/COCO_val_mcq_llama3.1_rephrased.csv \\
            --output-csv  logs/evaluation/mcq_llava/predictions/coco-mcq_reprocessed.csv \\
            --llava-model-path /path/to/llava-1.5-7b-hf \\
            --device cuda \\
            --dtype float16 \\
            --quantize int4
"""

from __future__ import annotations

import argparse
import logging
import re
import sys
from pathlib import Path
from typing import Dict, List, Optional, Tuple

import numpy as np
import pandas as pd
import torch
import torch.nn.functional as F
from PIL import Image
from tqdm import tqdm

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(message)s",
)
logger = logging.getLogger(__name__)

OPTION_LABELS = ["A", "B", "C", "D"]

# Original caption slot ordering in every NegBench natural-image MCQ CSV.
# caption_0 is always the ground-truth; the others are distractors.
ORIGINAL_CAPTION_TYPES = ["gt", "hybrid", "positive", "negative"]


# Robust option-letter parsing
# Patterns are tried in priority order.  Each must have exactly one capture group
# that returns the predicted option letter.

_PRIORITY_PATTERNS: List[str] = [
    # "the answer is A / B / C / D" (with optional punctuation / parens)
    r"(?:correct\s+)?answer\s+(?:is|:)\s*[:\(]?\s*([A-D])\b",
    # "I choose / select / pick / go with A"
    r"(?:choose|select|pick|go\s+with)\s+(?:option\s+)?[:\(]?\s*([A-D])\b",
    # "option A" / "choice A"
    r"(?:option|choice)\s+([A-D])\b",
    # Text starts with "(A)" (model outputs the option label in parens)
    r"^\s*\(\s*([A-D])\s*\)",
    # Text starts with "A." / "A:" / "A)" followed by whitespace
    r"^\s*([A-D])\s*[\.\:\)]\s",
    # Entire text is just the letter (ideal case)
    r"^\s*([A-D])\s*$",
]

# Word-boundary fallback: letter surrounded by whitespace / punctuation
_BOUNDARY_RE = re.compile(
    r"(?:^|[\s\(\[,;!?])([A-D])(?=$|[\s\.\,\:\;\)\]\}!?])",
    re.MULTILINE,
)


def parse_option_robust(text: str, option_labels: List[str] = None) -> int:
    """
    Extract the predicted option index from generated text.

    Tries high-confidence regex patterns first.  Falls back to finding the
    last word-boundary-isolated option letter in the text (models tend to
    self-correct at the end of their response).  Returns 0 with a warning if
    no letter can be identified.

    Args:
        text:          Raw string generated by the model.
        option_labels: Labels in use (default: ["A", "B", "C", "D"]).

    Returns:
        Zero-based index into option_labels.
    """
    if option_labels is None:
        option_labels = OPTION_LABELS
    label_set = {lbl.upper() for lbl in option_labels}
    text_upper = text.strip().upper()

    for pattern in _PRIORITY_PATTERNS:
        m = re.search(pattern, text_upper, re.IGNORECASE)
        if m:
            letter = m.group(1).upper()
            if letter in label_set:
                return option_labels.index(letter)

    # Word-boundary fallback: take the LAST isolated letter found
    matches = list(_BOUNDARY_RE.finditer(text_upper))
    valid = [m for m in matches if m.group(1) in label_set]
    if valid:
        return option_labels.index(valid[-1].group(1))

    logger.warning("Could not parse option from: %r  -> defaulting to index 0.", text)
    return 0


# Original-order restoration helpers

def _build_dataset_index(dataset_csv: str) -> pd.DataFrame:
    """
    Load the original NegBench MCQ CSV.

    Normalises the image-path column name to 'image_path' and adds a
    'caption_set' column (frozenset of the 4 captions) for fast matching.
    """
    df = pd.read_csv(dataset_csv, sep=",")

    # Normalise image-path column
    path_col_candidates = ["image_path", "filepath", "path", "file_path"]
    path_col = next((c for c in path_col_candidates if c in df.columns), None)
    if path_col is None:
        raise ValueError(
            f"Cannot locate image-path column in {dataset_csv}. "
            f"Available columns: {list(df.columns)}"
        )
    if path_col != "image_path":
        df = df.rename(columns={path_col: "image_path"})

    # Detect caption columns
    cap_cols = sorted(
        [c for c in df.columns if re.fullmatch(r"caption_\d+", c)],
        key=lambda c: int(c.split("_")[1]),
    )
    if not cap_cols:
        raise ValueError(
            f"No caption_N columns found in {dataset_csv}. "
            f"Available columns: {list(df.columns)}"
        )

    df["_caption_set"] = df[cap_cols].apply(
        lambda row: frozenset(str(v) for v in row), axis=1
    )
    df["_cap_cols"] = [cap_cols] * len(df)
    return df


def _match_original_row(
    shuffled_captions: List[str],
    image_path: str,
    ds_df: pd.DataFrame,
) -> Optional[pd.Series]:
    """
    Find the original dataset row whose caption set matches shuffled_captions.

    Filters first by image_path (fast), then by caption content.
    Returns None when no match is found (e.g. image path format mismatch).
    """
    target_set = frozenset(str(c) for c in shuffled_captions)

    # Try exact image-path match first
    candidates = ds_df[ds_df["image_path"] == image_path]

    # If that fails, try matching by basename (handles relative-vs-absolute mismatch)
    if candidates.empty:
        basename = Path(image_path).name
        candidates = ds_df[ds_df["image_path"].apply(lambda p: Path(p).name) == basename]

    if candidates.empty:
        return None

    for _, row in candidates.iterrows():
        if row["_caption_set"] == target_set:
            return row
    return None


def _build_perm(shuffled: List[str], original: List[str]) -> List[int]:
    """
    Return perm where perm[orig_idx] = shuffled_idx,
    i.e. shuffled[perm[i]] == original[i].
    """
    perm = []
    remaining = list(enumerate(shuffled))
    for orig_cap in original:
        for j, (s_idx, s_cap) in enumerate(remaining):
            if orig_cap == s_cap:
                perm.append(s_idx)
                remaining.pop(j)
                break
        else:
            raise ValueError(f"Caption not found in shuffled list: {orig_cap!r}")
    return perm


def restore_original_order(
    shuffled_captions: List[str],
    shuffled_correct_answer: int,
    shuffled_predicted_answer: int,
    image_path: str,
    ds_df: pd.DataFrame,
) -> Tuple[List[str], int, int, List[str], bool]:
    """
    Restore original caption order given a predictions row.

    Returns:
        orig_captions:    Captions in original dataset order.
        orig_correct:     Correct-answer index in original order.
        orig_predicted:   Predicted-answer index in original order.
        orig_types:       Caption types in original order.
        matched:          True if the original row was found.
    """
    orig_row = _match_original_row(shuffled_captions, image_path, ds_df)
    if orig_row is None:
        logger.warning(
            "No matching original row found for image '%s'. "
            "Keeping shuffled order.", image_path
        )
        return (
            shuffled_captions,
            shuffled_correct_answer,
            shuffled_predicted_answer,
            ["unknown"] * len(shuffled_captions),
            False,
        )

    cap_cols = orig_row["_cap_cols"]
    original_captions = [str(orig_row[c]) for c in cap_cols]
    orig_types = list(ORIGINAL_CAPTION_TYPES[: len(original_captions)])

    # perm[orig_idx] = shuffled_idx
    perm = _build_perm(shuffled_captions, original_captions)

    # Convert shuffled index to original index:  find i where perm[i] == shuffled_idx
    def to_orig(shuffled_idx: int) -> int:
        return next(i for i, s in enumerate(perm) if s == shuffled_idx)

    orig_correct = to_orig(shuffled_correct_answer)
    orig_predicted = to_orig(shuffled_predicted_answer)

    return original_captions, orig_correct, orig_predicted, orig_types, True


# Logit extraction helpers

def _get_option_token_ids(
    tokenizer,
    option_labels: List[str] = None,
) -> Dict[str, int]:
    """
    Resolve the single-token ID for each option letter in the tokenizer vocabulary.
    Tries space-prefixed form first (common in LLaMA-family tokenizers).
    """
    if option_labels is None:
        option_labels = OPTION_LABELS
    token_ids: Dict[str, int] = {}
    for label in option_labels:
        resolved = False
        for candidate in [f" {label}", label, f"({label})"]:
            ids = tokenizer.encode(candidate, add_special_tokens=False)
            if len(ids) == 1:
                token_ids[label] = ids[0]
                resolved = True
                break
        if not resolved:
            ids = tokenizer.encode(label, add_special_tokens=False)
            token_ids[label] = ids[0]
            logger.warning(
                "Option '%s' encodes to multiple tokens; using first token id=%d.",
                label, ids[0],
            )
    logger.info("Option token ids: %s", token_ids)
    return token_ids


def extract_option_logits(
    model,
    processor,
    image_path: str,
    captions: List[str],
    option_labels: List[str],
    option_token_ids: Dict[str, int],
    device: torch.device,
    dtype: torch.dtype,
    use_device_map: bool,
) -> np.ndarray:
    """
    Run a single-token generation forward pass and return the softmax probability
    of each option letter token.

    Captions must be in the SHUFFLED order that will be shown to the model
    (same as during the original evaluation run).

    Returns:
        np.ndarray of shape [n_options], probabilities summing to 1.
    """
    n = len(captions)
    labels = option_labels[:n]

    options_str = "\n".join(f"({lbl}) {cap}" for lbl, cap in zip(labels, captions))
    prompt_text = (
        "Which caption best describes the image?\n"
        f"{options_str}\n"
        "Answer with only the letter of the correct option."
    )
    conversation = [
        {
            "role": "user",
            "content": [
                {"type": "image"},
                {"type": "text", "text": prompt_text},
            ],
        }
    ]

    if hasattr(processor, "apply_chat_template"):
        text_prompt = processor.apply_chat_template(
            conversation, add_generation_prompt=True
        )
    else:
        text_prompt = processor.tokenizer.apply_chat_template(
            conversation, add_generation_prompt=True, tokenize=False
        )

    pil_image = Image.open(image_path).convert("RGB")
    inputs = processor(images=pil_image, text=text_prompt, return_tensors="pt")

    if not use_device_map:
        inputs = inputs.to(device)
    else:
        # When device_map is active, move only non-special tensors
        inputs = {
            k: (v.to(device) if isinstance(v, torch.Tensor) else v)
            for k, v in inputs.items()
        }

    if "pixel_values" in inputs:
        inputs["pixel_values"] = inputs["pixel_values"].to(dtype)

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=1,
            do_sample=False,
            output_scores=True,
            return_dict_in_generate=True,
        )

    # scores[0]: logits for the 1st generated token, shape [batch, vocab_size]
    first_logits = outputs.scores[0][0]

    raw = torch.tensor(
        [first_logits[option_token_ids[lbl]].item() for lbl in labels],
        dtype=torch.float32,
    )
    probs = F.softmax(raw, dim=0).cpu().numpy()
    return probs


# Main pipeline

def parse_args(args):
    parser = argparse.ArgumentParser(
        description="Reprocess LLaVA MCQ predictions: fix parsing, restore original order, optionally extract logits."
    )
    parser.add_argument(
        "--input-csv", required=True,
        help="Path to the existing predictions CSV (output of eval_negation_llava.py).",
    )
    parser.add_argument(
        "--dataset-csv", required=True,
        help="Path to the original NegBench MCQ dataset CSV (used to restore original caption order).",
    )
    parser.add_argument(
        "--output-csv", required=True,
        help="Path for the reprocessed output CSV.",
    )
    parser.add_argument(
        "--llava-model-path", default=None,
        help="Path to a LLaVA checkpoint.  Required only for logit extraction. "
             "If omitted, logit columns are filled with NaN.",
    )
    parser.add_argument("--device", default="cuda")
    parser.add_argument(
        "--dtype", default="float16", choices=["float16", "bfloat16", "float32"]
    )
    parser.add_argument(
        "--quantize", default=None, choices=["int4", "int8"],
        help="BitsAndBytes quantization (requires bitsandbytes). "
             "Use int4 for GPUs with less than 16 GB VRAM.",
    )
    return parser.parse_args(args)


def load_llava(model_path: str, device: torch.device, dtype: torch.dtype, quantize: Optional[str]):
    """Load LLaVA model and processor (mirrors llava_evaluator.py logic)."""
    from transformers import LlavaForConditionalGeneration, LlavaProcessor

    try:
        from transformers import LlavaNextForConditionalGeneration, LlavaNextProcessor
        _have_next = True
    except ImportError:
        _have_next = False

    import json
    cfg_file = Path(model_path) / "config.json"
    variant = "llava"
    if cfg_file.exists():
        with open(cfg_file, encoding="utf-8") as f:
            cfg = json.load(f)
        if "llava_next" in cfg.get("model_type", ""):
            variant = "llava_next"

    model_kwargs: dict = {"torch_dtype": dtype, "low_cpu_mem_usage": True}
    use_device_map = False

    if quantize is not None:
        from transformers import BitsAndBytesConfig
        bnb = BitsAndBytesConfig(
            load_in_4bit=(quantize == "int4"),
            load_in_8bit=(quantize == "int8"),
            bnb_4bit_compute_dtype=torch.float16,
            bnb_4bit_use_double_quant=True,
            bnb_4bit_quant_type="nf4",
        )
        model_kwargs["quantization_config"] = bnb
        model_kwargs["device_map"] = "auto"
        use_device_map = True

    if variant == "llava_next" and _have_next:
        processor = LlavaNextProcessor.from_pretrained(model_path)
        model = LlavaNextForConditionalGeneration.from_pretrained(model_path, **model_kwargs)
    else:
        processor = LlavaProcessor.from_pretrained(model_path)
        model = LlavaForConditionalGeneration.from_pretrained(model_path, **model_kwargs)

    if not use_device_map:
        model = model.to(device)
    model.eval()
    return model, processor, use_device_map


def main(args):
    args = parse_args(args)

    logger.info("Loading input CSV: %s", args.input_csv)
    pred_df = pd.read_csv(args.input_csv)

    logger.info("Loading dataset CSV: %s", args.dataset_csv)
    ds_df = _build_dataset_index(args.dataset_csv)

    cap_cols_pred = sorted(
        [c for c in pred_df.columns if re.fullmatch(r"caption_\d+", c)],
        key=lambda c: int(c.split("_")[1]),
    )
    n_options = len(cap_cols_pred)
    labels = OPTION_LABELS[:n_options]

    # Optionally load model for logit extraction
    model = processor = option_token_ids = None
    use_device_map = False
    dtype = {"float16": torch.float16, "bfloat16": torch.bfloat16, "float32": torch.float32}[args.dtype]
    device = torch.device(args.device)

    if args.llava_model_path is not None:
        logger.info("Loading LLaVA model for logit extraction from %s ...", args.llava_model_path)
        model, processor, use_device_map = load_llava(
            args.llava_model_path, device, dtype, args.quantize
        )
        option_token_ids = _get_option_token_ids(processor.tokenizer, labels)

    records = []

    for _, row in tqdm(pred_df.iterrows(), total=len(pred_df), desc="Reprocessing"):
        image_path = str(row["image_path"])
        shuffled_captions = [str(row[c]) for c in cap_cols_pred]
        shuffled_correct = int(row["correct_answer"])
        raw_generated = str(row.get("raw_generated", ""))
        question_type = str(row.get("question_type", ""))

        # Fix 1: robust parsing of raw_generated
        robust_predicted_shuffled = parse_option_robust(raw_generated, labels)

        # Fix 2: restore original caption order
        orig_captions, orig_correct, orig_predicted, orig_types, matched = (
            restore_original_order(
                shuffled_captions,
                shuffled_correct,
                robust_predicted_shuffled,
                image_path,
                ds_df,
            )
        )
        is_correct = orig_predicted == orig_correct

        record: dict = {
            "image_path": image_path,
            "question_type": question_type,
            "correct_answer": orig_correct,
            "predicted_answer": orig_predicted,
            "is_correct": is_correct,
            "raw_generated": raw_generated,
        }

        # Captions in original order
        for i, (cap, ctype) in enumerate(zip(orig_captions, orig_types)):
            record[f"caption_{i}"] = cap
            record[f"caption_type_{i}"] = ctype

        # Fix 3: logits (optional, requires model re-run)
        if model is not None:
            # Logit extraction uses the SHUFFLED captions (same as original evaluation)
            # but the resulting probabilities are re-ordered to original order
            try:
                shuffled_probs = extract_option_logits(
                    model, processor,
                    image_path, shuffled_captions,
                    labels, option_token_ids,
                    device, dtype, use_device_map,
                )
                # shuffled_probs[i] is the probability for the option shown at shuffled position i
                # We need to find which shuffled index corresponds to each original index
                perm = _build_perm(shuffled_captions, orig_captions)
                # perm[orig_idx] = shuffled_idx
                for orig_i, s_i in enumerate(perm):
                    record[f"logit_{orig_i}"] = float(shuffled_probs[s_i])
            except Exception as exc:
                logger.warning("Logit extraction failed for %s: %s", image_path, exc)
                for i in range(n_options):
                    record[f"logit_{i}"] = float("nan")
        else:
            for i in range(n_options):
                record[f"logit_{i}"] = float("nan")

        records.append(record)

    out_df = pd.DataFrame(records)
    Path(args.output_csv).parent.mkdir(parents=True, exist_ok=True)
    out_df.to_csv(args.output_csv, index=False)
    logger.info("Saved %d rows to %s", len(out_df), args.output_csv)

    # Print accuracy summary
    total = len(out_df)
    correct = out_df["is_correct"].sum()
    logger.info("Overall accuracy: %.4f  (%d / %d)", correct / total, correct, total)

    by_type = out_df.groupby("question_type")["is_correct"].mean()
    for qtype, acc in by_type.items():
        logger.info("  %s accuracy: %.4f", qtype, acc)


if __name__ == "__main__":
    main(sys.argv[1:])
